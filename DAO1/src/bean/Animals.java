package bean;

/**
 *
 * @author Win7
 */
public class Animals {

    private int id;
    private String name;
    private int age;
    private int anId;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the anId
     */
    public int getAnId() {
        return anId;
    }

    /**
     * @param anId the anId to set
     */
    public void setAnId(int anId) {
        this.anId = anId;
    }
}
